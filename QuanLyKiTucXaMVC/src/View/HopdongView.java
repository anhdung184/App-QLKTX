    private void BanghienthiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BanghienthiMouseClicked
        // TODO add your handling code here:
        KhoaMo(true);
        txtMahd.setEditable(false);
        selectedIndex=Banghienthi.getSelectedRow();
        HopdongModel p= quanlyhopdong.get(selectedIndex);
        DateFormat dateFormat = null;
        dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        txtMahd.setText(p.getMahd());
        txtManv.setText(p.getManv());
         tkquanlynhanvien=new NhanvienDAO().Timkiem(p.getManv());
        for(NhanvienModel nv:tkquanlynhanvien){
           txtHotenNV.setText(nv.getHoten());
                 }
        
        txtMaphong.setText(p.getMap());
         tkquanlyphong=new PhongDAO().Timkiem(p.getMap());
        for(PhongModel mt:tkquanlyphong){
           txtMota.setText(mt.getMota());
                 }
        txtMasv.setText(p.getMasv());
        tkquanlysinhvien=new SinhvienDAO().Timkiem(p.getMasv());
        for(SinhvienModel sv:tkquanlysinhvien){
           txtHotenSV.setText(sv.getHoten());
                 }
        txtTienphong.setText(Math.round(p.getTienphong())+"");
        txtTiencoc.setText(Math.round(p.getTiencoc())+"");
        txtNgaylap.setText(dateFormat.format(p.getNgaylap()));
        txtNgayDen.setText(dateFormat.format(p.getNgayden()));
        txtNgaytra.setText(dateFormat.format(p.getNgaytra()));
      
       jComboTinhtrang.setSelectedItem(p.getTinhtrang());
    }//GEN-LAST:event_BanghienthiMouseClicked

    private void btnGhiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGhiActionPerformed
        // TODO add your handling code here:
        
       if(txtManv.getText().isEmpty() || txtHotenNV.getText().isEmpty()){
             JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn nhân viên trong bảng nhân viên bên tay trái");
              txtManv.requestFocus();
              return;
        }
      
        if(txtMaphong.getText().isEmpty() || txtMota.getText().isEmpty() ){
             JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn phòng trong bảng phòng bên tay trái");
             txtMaphong.requestFocus();
              return;
        }
        if(txtMasv.getText().isEmpty()||txtHotenSV.getText().isEmpty()){
             JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn sinh viên trong bảng sinh viên viên bên tay trái");
             txtMasv.requestFocus();
              return;
        }
        if(txtTienphong.getText().isEmpty()){
             JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập tiền phòng ");
             txtTienphong.requestFocus();
              return;
        }
        if(txtNgaylap.getText().isEmpty()){
             JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập ngày lập");
             txtNgaylap.requestFocus();
              return;
        }
        if(txtNgayDen.getText().isEmpty()){
             JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập ngày đến");
             txtNgayDen.requestFocus();
              return;
        }
        if(txtNgaytra.getText().isEmpty()){
             JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập ngày trả");
             txtNgaytra.requestFocus();
              return;
        }
        
        try {
            HopdongModel hd=new HopdongModel();
            Date dateNow = new Date();
            Date nl,nd,nt;
            nl = df.parse(txtNgaylap.getText());
            nd = df.parse(txtNgayDen.getText());
            nt = df.parse(txtNgaytra.getText());
             if(nl.getTime() <= dateNow.getTime()){
                hd.setMahd(txtMahd.getText());
                hd.setManv(txtManv.getText());
                hd.setMap(txtMaphong.getText());
                hd.setMasv(txtMasv.getText());
                hd.setNgaylap(nl);
                hd.setNgayden(nd);
                hd.setNgaytra(nt);
                
             
                hd.setTinhtrang(jComboTinhtrang.getSelectedItem().toString());
                boolean isNXB=true;
                try {
                        hd.setTienphong(Double.parseDouble(txtTienphong.getText()));

                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(this, "Giá tiền chỉ là số ko phải là kí tự");
                        txtTienphong.requestFocus();
                        isNXB=false;
                        return;
                       
                    }
                
                try {
                          hd.setTiencoc(Double.parseDouble(txtTiencoc.getText()));

                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(this, "Số người ở hiện tại chỉ là số ko phải là kí tự");
                        txtTiencoc.requestFocus();
                        isNXB=false;
                        return;
                    }
                PhongModel p=new PhongModel();
               p.setMap(txtMaphong.getText());
               if(isNXB){
                   new HopDongDAO().ThemHopdong(hd);
                    showTable();
                    JOptionPane.showMessageDialog(this, "Thêm thành công");
                    new PhongDAO().SuaphongkhithemHD(p);
                    XoaTrang();
                    KhoaMo(true);
                    bangphu();
               }
            }else{
                
                JOptionPane.showMessageDialog(rootPane, "Ngày sinh phải nhỏ hơn hoặc bằng ngày hiện thời!");
                }
            
        } catch (ParseException ex) {
                JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập đúng định dạng (vd: dd/MM/yyyy)!");

        }
        
    }//GEN-LAST:event_btnGhiActionPerformed

