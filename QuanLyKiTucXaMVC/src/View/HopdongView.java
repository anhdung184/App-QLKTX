    private void bangphu(){
    
        if(jRadioSinhvien.isSelected()){
            String tksv=txtTimkiemChung.getText();
                 tkquanlysinhvien=new SinhvienDAO().TimkiemSV(tksv);
                 modelchung.setRowCount(0);
                 for(SinhvienModel s:tkquanlysinhvien){
                        modelchung.addRow(new Object[]{
                       Banghienthichung.getRowCount()+1,s.getMasv(),s.getHoten(),
                     });
                 }
                 return;
            
        }
        if(jRadioNhanvien.isSelected()){
            String tksv=txtTimkiemChung.getText();
                 tkquanlynhanvien=new NhanvienDAO().Timkiem(tksv);
                 modelchung.setRowCount(0);
                 for(NhanvienModel nv:tkquanlynhanvien){
                        modelchung.addRow(new Object[]{
                       Banghienthichung.getRowCount()+1,nv.getManv(),nv.getHoten(),
                     });
                 }
                 return;
            
        }
        if(jRadioPhong.isSelected()){
            String tksv=txtTimkiemChung.getText();
                 tkquanlyphong=new PhongDAO().TimkiemPhong(tksv);
                 modelchung.setRowCount(0);
                 for(PhongModel s:tkquanlyphong){
                        modelchung.addRow(new Object[]{
                      s.getMap(),s.getLoaiphong(),s.getMota(),
                     });
                 }
                 return;
            
        }
    }
    
    
